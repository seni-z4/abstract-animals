public class Delfino extends Animale implements Nuota {
  @Override
  public void dormi() {
    System.out.println("Delfino sta dormendo");
  }

  @Override
  public void verso() {
    System.out.println("puf puf");

  }

  @Override
  public void mangia() {
    System.out.println("Delfino sta mangiando");

  }

  @Override
  public void Nuota() {
    System.out.println("Sto nuotando!!!");
  }

}
